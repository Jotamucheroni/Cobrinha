void gerar_comida(ponto *comida, char planta[][num_linha + 1])
{
	//Define o ponto inicial da comida
	srand(time(NULL));
	(*comida).x = rand() % (num_coluna) + 1;
	(*comida).y = rand() % (num_linha) + 1;
	while(planta[(*comida).x][(*comida).y]) //Não pode coincidir com mapa, nem com a cobra
	{
		(*comida).x++;
		(*comida).y++;
		if((*comida).x >= num_coluna)
			(*comida).x = 1;
		if((*comida).y >= num_linha)
			(*comida).y = 1;
	}
}

void tela(char planta[][num_linha + 1], cobra c)
{
	unsigned char i, j;
	cor cor_aux;
	
	volta_padrao(TUDO);
	clrscr();
	cor_aux._16CORES.nome = BRANCO;
	cor_aux._16CORES.mod = NORMAL;
	determina_cor(FUNDO, _16CORES, cor_aux);
	
	for(i = 1; i <= num_coluna; i++)
		for(j = 1; j <= num_linha; j++)
			if(planta[i][j])
			{
				gotoxy(i, j);
				printf(" ");
			}

	volta_padrao(FUNDO);		
	for(i = 0; i < c.tam; i++)
	{
		gotoxy(c.corpo[i].x, c.corpo[i].y);
		printf("@");
	}
	
}

void jogar()
{
	cobra c;
	int tempo = 100000, i;
	char planta[num_coluna + 1][num_linha + 1];
    unsigned char x, y, seta = 0, m_comida = 255, mapa = 0;
	ponto aux, aux0, comida;
	cor cor_aux;
	//Escolhe o mapa
	do
	{
		volta_padrao(TUDO);
		clrscr();
		for (x=1; x <= num_coluna; x++)
			for (y=1; y <= num_linha; y++)
				planta[x][y] = mapas[mapa][y][x];
		c.tam = 0;
		tela(planta,c);
		
		volta_padrao(TUDO);
		formata_texto(NEGRITO, ATIVADO);
		cor_aux._16CORES.nome = BRANCO;
		cor_aux._16CORES.mod = NORMAL;
		determina_cor(TEXTO, _16CORES, cor_aux);
		
		gotoxy(37,1);
		printf("Mapa %d", mapa+1);
		seta = getch();
		switch(seta)
		{
			case 'a':
				if(mapa > 0)
					mapa--;
				else
					mapa = num_mapa -1;
				break;
			case 'd':
				if(mapa < num_mapa - 1)
					mapa++;
				else
					mapa = 0;
				break;
		}
	}while(seta != 10);
	
	
	//Define o mapa
		for (x=1; x <= num_coluna; x++)
			for (y=1; y <= num_linha; y++)
				planta[x][y] = mapas[mapa][y][x];
		
	
		//Define a cobra
		c.tam = tam_inicial_cobra;
		for(i = 0; i < c.tam; i++)
		{
			c.corpo[i].x = 40 - i;
			c.corpo[i].y = 3;
		}
		c.velV = 0;
		c.velH = 1;
	
		//Desenha mapa e cobra	
		tela(planta, c);
		
		for(i = 0; i < c.tam; i++)//Coloca a cobra no mapa
		{		
			planta[c.corpo[i].x][c.corpo[i].y] = 1;
		}
		gerar_comida(&comida, planta);
		seta = 0;
		//Desenha a pontuação
		volta_padrao(TUDO);
		formata_texto(NEGRITO, ATIVADO);
		cor_aux._16CORES.nome = PRETO;
		cor_aux._16CORES.mod = NORMAL;
		determina_cor(TEXTO, _16CORES, cor_aux);
		cor_aux._16CORES.nome = BRANCO;
		determina_cor(FUNDO, _16CORES, cor_aux);
		
		gotoxy(67, 1);
		printf("Pontos:    0");
		while(1)
		{
			if(kbhit()) //Verifica se o usuário apertou uma tecla
				seta = getchar();
			switch(seta)
			{
				case 'w':
					if(!(c.velV == 1 && c.velH == 0)) 
					{
						c.velH = 0;
						c.velV = -1;
						tempo = 150000;
					}
					break;
				case 's':
					if(!(c.velV == -1 && c.velH == 0)) 
					{
						c.velH = 0;
						c.velV = 1;
						tempo = 150000;
					}
					break;
				case 'd':
					if(!(c.velV == 0 && c.velH == -1)) 
					{
						c.velH = 1;
						c.velV = 0;
						tempo = 100000;
					}
					break;
				case 'a':
					if(!(c.velV == 0 && c.velH == 1)) 
					{
						c.velH = -1;
						c.velV = 0;
						tempo = 100000;
					}
					break;
				case 10:
					getchar();
					seta = 0;
					break;
			}
			fflush(stdout); //Usar sempre antes de um sleep, senão trava as outras ações do programa
			usleep(tempo);
			
			volta_padrao(TUDO);
			
			gotoxy(c.corpo[c.tam - 1].x, c.corpo[c.tam - 1].y);
			printf(" ");
			planta[c.corpo[c.tam - 1].x][c.corpo[c.tam - 1].y] -= 1;
			aux0 = c.corpo[0];
			c.corpo[0].x += c.velH;
			c.corpo[0].y += c.velV;
			planta[c.corpo[0].x][c.corpo[0].y] += 1;
			if(planta[c.corpo[0].x][c.corpo[0].y] > 1)
				return;
			gotoxy(c.corpo[0].x, c.corpo[0].y);
			printf("@");
			if(c.corpo[0].x == comida.x && c.corpo[0].y == comida.y) //Se a cobrinha comer a comida
			{
				gerar_comida(&comida, planta);
				c.tam++;
				
				volta_padrao(TUDO);
				formata_texto(NEGRITO, ATIVADO);
				cor_aux._16CORES.nome = PRETO;
				cor_aux._16CORES.mod = NORMAL;
				determina_cor(TEXTO, _16CORES, cor_aux);
				cor_aux._16CORES.nome = BRANCO;
				determina_cor(FUNDO, _16CORES, cor_aux);
				
				gotoxy(75, 1);
				if(c.tam - tam_inicial_cobra < 1000)
					printf(" ");
				if(c.tam - tam_inicial_cobra < 100)
					printf(" ");
				if(c.tam - tam_inicial_cobra < 10)
					printf(" ");
				printf("%d", c.tam - tam_inicial_cobra);
			}
			for(i = 1; i < c.tam; i++) 						//1 -> 0, 2 -> 1, 3 -> 2, 4 -> 3, ...
			{
				aux = c.corpo[i];
				c.corpo[i] = aux0;
				aux0 = aux;
			}
			
			volta_padrao(TUDO);
			
			//Faz a comida piscar
			gotoxy(comida.x, comida.y);
			if(m_comida)
				printf("0");
			else
				printf(" ");
			m_comida = ~m_comida;
		}
}

